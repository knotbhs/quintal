 * Class Servicos.
 *
 * @package namespace App\Entities;
 */
class Servicos extends Model implements Transformable
{
    use TransformableTrait;

    /**
     * The attributes that are mass assignable.
     *
     * @var array
     */
    protected $fillable = [
        "name",
        "valor",
        "descricao"
    ];
}
